package gu.sign;

public class SignFormVO {

	private String frmno;
	private String frmname;
	private String frmdesc;
	private String frmcontents;
	
	public String getFrmno() {
		return frmno;
	}
	public void setFrmno(String frmno) {
		this.frmno = frmno;
	}
	public String getFrmname() {
		return frmname;
	}
	public void setFrmname(String frmname) {
		this.frmname = frmname;
	}
	public String getFrmdesc() {
		return frmdesc;
	}
	public void setFrmdesc(String frmdesc) {
		this.frmdesc = frmdesc;
	}
	public String getFrmcontents() {
		return frmcontents;
	}
	public void setFrmcontents(String frmcontents) {
		this.frmcontents = frmcontents;
	}

	
}
